function analyzeFile(file) {
  const absolutePath = path.join(ROOT, file);
  const content = fs.readFileSync(absolutePath, 'utf8');
  const lineCount = content.split(/\r?\n/).length;
  const ext = path.extname(file).toLowerCase();
  const meta = FILE_METADATA[file] || {
    category: 'Uncategorized',
    purpose: 'No manual summary yet.',
  };

  let analysis = {
    file,
    absolutePath,
    content,
    ext,
    lineCount,
    meta,
    highlights: [],
    dependencies: [],
    envVars: [],
    routes: [],
    exports: [],
    classes: [],
    functions: [],
    notes: [],
  };

  if (file === 'package.json') {
    analysis = { ...analysis, ...analyzePackageJson(content) };
  } else if (file === 'models.json') {
    analysis = { ...analysis, ...analyzeModelsJson(content) };
  } else if (ext === '.js') {
    analysis = { ...analysis, ...analyzeJavaScript(content) };
  } else if (ext === '.py') {
    analysis = { ...analysis, ...analyzePython(content) };
  } else if (file === 'Dockerfile' || file === 'Docker') {
    analysis = { ...analysis, ...analyzeDockerfile(content) };
  } else if (ext === '.yml' || ext === '.yaml') {
    analysis = { ...analysis, ...analyzeWorkflow(content) };
  } else if (ext === '.bat') {
    analysis = { ...analysis, ...analyzeBatch(content) };
  }

  if (analysis.classes.length > 0) {
    analysis.highlights.push(`${analysis.classes.length} classes`);
  }
  if (analysis.functions.length > 0) {
    analysis.highlights.push(`${analysis.functions.length} top-level functions`);
  }
  if (analysis.routes.length > 0) {
    analysis.highlights.push(`${analysis.routes.length} Express routes/middleware declarations`);
  }
  if (analysis.envVars.length > 0) {
    analysis.highlights.push(`${analysis.envVars.length} environment variables referenced`);
  }
  if (analysis.dependencies.length > 0 && file !== 'package.json') {
    analysis.highlights.push(`${analysis.dependencies.length} imports/requires`);
  }

  return analysis;
}

function analyzePackageJson(content) {
  const pkg = JSON.parse(content);
  const deps = Object.entries(pkg.dependencies || {}).map(([name, version]) => `${name}@${version}`);
  const scripts = Object.entries(pkg.scripts || {}).map(([name, command]) => `${name}: ${command}`);
  return {
    notes: [
      `Package name: \`${pkg.name || 'unknown'}\``,
      `Version: \`${pkg.version || 'unknown'}\``,
      `Dependencies: ${deps.length}`,
      scripts.length > 0 ? `Scripts: ${scripts.length}` : 'Scripts: none declared',
    ],
    dependencies: deps,
    functions: scripts.map((item) => ({ name: item, line: null, params: '' })),
  };
}

function analyzeModelsJson(content) {
  const models = JSON.parse(content);
  return {
    notes: [
      Array.isArray(models)
        ? `Model count: ${models.length}`
        : `Top-level type: ${typeof models}`,
      Array.isArray(models) && models.length > 0
        ? `First models: ${models.slice(0, 5).map((item) => `\`${item}\``).join(', ')}`
        : 'No model ids found.',
    ],
  };
}

function analyzeJavaScript(content) {
  const dependencies = unique(findMatches(/require\(["']([^"']+)["']\)/g, content, (match) => match[1]));
  const envVars = unique(findMatches(/process\.env\.([A-Z0-9_]+)/g, content, (match) => match[1]));
  const exports = unique(findMatches(/module\.exports\s*=\s*\{([^}]+)\}/g, content, (match) => match[1].split(',').map((item) => item.trim()).filter(Boolean)).flat());
  const routes = extractRoutes(content);
  const classes = extractClasses(content);
  const functions = extractTopLevelFunctions(content);

  return {
    dependencies,
    envVars,
    exports,
    routes,
    classes,
    functions,
  };
}

function analyzePython(content) {
  const imports = unique([
    ...findMatches(/^import\s+([^\n]+)/gm, content, (match) => match[1].split(',').map((item) => item.trim())),
    ...findMatches(/^from\s+([^\s]+)\s+import\s+([^\n]+)/gm, content, (match) => [`${match[1]}:${match[2].trim()}`]),
  ].flat());
  const functions = findRegexWithLines(/^(def)\s+([A-Za-z_][A-Za-z0-9_]*)\(([^)]*)\):/gm, content).map((entry) => ({
    name: entry.match[2],
    params: entry.match[3].trim(),
    line: entry.line,
  }));
  return {
    dependencies: imports,
    functions,
  };
}

function analyzeDockerfile(content) {
  const baseImages = findMatches(/^FROM\s+([^\n]+)/gm, content, (match) => match[1].trim());
  const copiedPaths = findMatches(/^COPY\s+([^\n]+)/gm, content, (match) => match[1].trim());
  const exposedPorts = findMatches(/^EXPOSE\s+([^\n]+)/gm, content, (match) => match[1].trim());
  const commands = findMatches(/^CMD\s+([^\n]+)/gm, content, (match) => match[1].trim());
  return {
    notes: [
      baseImages.length > 0 ? `Base image(s): ${baseImages.map((item) => `\`${item}\``).join(', ')}` : 'Base image not detected.',
      exposedPorts.length > 0 ? `Exposed ports: ${exposedPorts.map((item) => `\`${item}\``).join(', ')}` : 'No EXPOSE directives found.',
    ],
    dependencies: [...baseImages.map((item) => `FROM ${item}`), ...copiedPaths.map((item) => `COPY ${item}`)],
    functions: commands.map((cmd) => ({ name: `CMD ${cmd}`, params: '', line: null })),
  };
}

function analyzeWorkflow(content) {
  const jobs = extractWorkflowJobs(content);
  const actionUses = findMatches(/uses:\s*([^\n]+)/g, content, (match) => match[1].trim());
  return {
    notes: [
      jobs.length > 0 ? `Jobs: ${jobs.map((item) => `\`${item}\``).join(', ')}` : 'No jobs detected.',
      actionUses.length > 0 ? `Actions used: ${actionUses.map((item) => `\`${item}\``).join(', ')}` : 'No reusable actions detected.',
    ],
    dependencies: actionUses,
  };
}

function analyzeBatch(content) {
  const commands = content
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(
      (line) =>
        line &&
        line !== '(' &&
        line !== ')' &&
        !line.startsWith('::') &&
        !line.startsWith('@echo') &&
        !line.startsWith('echo'),
    );
  return {
    notes: commands.length > 0 ? [`Commands: ${commands.map((line) => `\`${line}\``).join(', ')}`] : [],
  };
}

function extractRoutes(content) {
  const regex = /app\.(get|post|put|delete|patch|all|use)\(\s*([^,\n]+)/g;
  return findRegexWithLines(regex, content).map((entry) => ({
    method: entry.match[1].toUpperCase(),
    path: normalizeRouteTarget(entry.match[2].trim()),
    line: entry.line,
  }));
}

function extractClasses(content) {
  const classes = [];
  const classRegex = /class\s+([A-Za-z_][A-Za-z0-9_]*)(?:\s+extends\s+([A-Za-z_][A-Za-z0-9_]*))?\s*\{/g;
  const lineIndex = createLineIndex(content);
  let match;

  while ((match = classRegex.exec(content)) !== null) {
    const openBraceIndex = content.indexOf('{', match.index);
    const endIndex = findMatchingBrace(content, openBraceIndex);
    const body = endIndex > openBraceIndex ? content.slice(openBraceIndex + 1, endIndex) : '';
    const bodyOffset = openBraceIndex + 1;
    const methods = [];
    const methodRegex = /^\s{2}(?:async\s+)?([A-Za-z_][A-Za-z0-9_]*)\(([^)]*)\)\s*\{/gm;
    const getterRegex = /^\s{2}(get|set)\s+([A-Za-z_][A-Za-z0-9_]*)\(([^)]*)\)\s*\{/gm;

    let methodMatch;
    while ((methodMatch = methodRegex.exec(body)) !== null) {
      methods.push({
        name: methodMatch[1],
        params: normalizeSignature(methodMatch[2]),
        line: lineNumberAt(lineIndex, bodyOffset + methodMatch.index),
        kind: 'method',
      });
    }
    while ((methodMatch = getterRegex.exec(body)) !== null) {
      methods.push({
        name: `${methodMatch[1]} ${methodMatch[2]}`,
        params: normalizeSignature(methodMatch[3]),
        line: lineNumberAt(lineIndex, bodyOffset + methodMatch.index),
        kind: methodMatch[1],
      });
    }
    methods.sort((a, b) => a.line - b.line);

    classes.push({
      name: match[1],
      extends: match[2] || null,
      line: lineNumberAt(lineIndex, match.index),
      methods,
    });
  }

  return classes;
}

function extractTopLevelFunctions(content) {
  const functionEntries = [];
  const functionRegex = /^(?:async\s+)?function\s+([A-Za-z_][A-Za-z0-9_]*)\(([^)]*)\)\s*\{/gm;
  const arrowRegex = /^const\s+([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(?:async\s*)?\(([^)]*)\)\s*=>\s*\{/gm;

  for (const entry of findRegexWithLines(functionRegex, content)) {
    functionEntries.push({
      name: entry.match[1],
      params: normalizeSignature(entry.match[2]),
      line: entry.line,
    });
  }
  for (const entry of findRegexWithLines(arrowRegex, content)) {
    functionEntries.push({
      name: entry.match[1],
      params: normalizeSignature(entry.match[2]),
      line: entry.line,
    });
  }

  functionEntries.sort((a, b) => {
    if (a.line == null && b.line == null) return 0;
    if (a.line == null) return 1;
    if (b.line == null) return -1;
    return a.line - b.line;
  });

  return uniqueBy(functionEntries, (item) => `${item.name}:${item.line}`);
}

function renderAnalysis(analysis) {
  const lines = [];
  lines.push(`### \`${analysis.file}\``);
  lines.push('');
  lines.push(`- Category: ${analysis.meta.category}`);
  lines.push(`- Purpose: ${analysis.meta.purpose}`);
  lines.push(`- Lines: ${analysis.lineCount}`);
  if (analysis.meta.entry) {
    lines.push(`- Entry command: \`${analysis.meta.entry}\``);
  }
  if (analysis.notes.length > 0) {
    lines.push('- Notes:');
    analysis.notes.forEach((note) => lines.push(`  - ${note}`));
  }
  if (analysis.dependencies.length > 0) {
    lines.push('- Imports / dependencies:');
    analysis.dependencies.forEach((dep) => lines.push(`  - \`${dep}\``));
  }
  if (analysis.envVars.length > 0) {
    lines.push('- Environment variables:');
    analysis.envVars.forEach((envName) => lines.push(`  - \`${envName}\``));
  }
  if (analysis.routes.length > 0) {
    lines.push('- Express routes / middleware:');
    analysis.routes.forEach((route) => {
      lines.push(`  - Line ${route.line}: \`${route.method}\` ${route.path}`);
    });
  }
  if (analysis.classes.length > 0) {
    lines.push('- Classes:');
    analysis.classes.forEach((klass) => {
      lines.push(
        `  - Line ${klass.line}: \`${klass.name}\`${klass.extends ? ` extends \`${klass.extends}\`` : ''}`,
      );
      if (klass.methods.length > 0) {
        klass.methods.forEach((method) => {
          lines.push(`    - Line ${method.line}: \`${method.name}(${method.params})\``);
        });
      }
    });
  }
  if (analysis.functions.length > 0) {
    lines.push('- Functions / commands:');
    analysis.functions.forEach((fn) => {
      if (fn.line) {
        lines.push(`  - Line ${fn.line}: \`${fn.name}(${fn.params})\``);
      } else {
        lines.push(`  - \`${fn.name}${fn.params ? `(${fn.params})` : ''}\``);
      }
    });
  }
  return lines.join('\n');
}

function createLineIndex(content) {
  const starts = [0];
  for (let index = 0; index < content.length; index += 1) {
    if (content[index] === '\n') {
      starts.push(index + 1);
    }
  }
  return starts;
}

function lineNumberAt(lineIndex, absoluteIndex) {
  let low = 0;
  let high = lineIndex.length - 1;
  while (low <= high) {
    const middle = Math.floor((low + high) / 2);
    if (lineIndex[middle] <= absoluteIndex) {
      if (middle === lineIndex.length - 1 || lineIndex[middle + 1] > absoluteIndex) {
        return middle + 1;
      }
      low = middle + 1;
    } else {
      high = middle - 1;
    }
  }
  return 1;
}

function findMatchingBrace(content, openBraceIndex) {
  let depth = 0;
  let inSingle = false;
  let inDouble = false;
  let inTemplate = false;
  let inLineComment = false;
  let inBlockComment = false;
  let escaped = false;

  for (let index = openBraceIndex; index < content.length; index += 1) {
    const char = content[index];
    const next = content[index + 1];

    if (inLineComment) {
      if (char === '\n') {
        inLineComment = false;
      }
      continue;
    }

    if (inBlockComment) {
      if (char === '*' && next === '/') {
        inBlockComment = false;
        index += 1;
      }
      continue;
    }

    if (inSingle) {
      if (!escaped && char === "'") {
        inSingle = false;
      }
      escaped = !escaped && char === '\\';
      continue;
    }

    if (inDouble) {
      if (!escaped && char === '"') {
        inDouble = false;
      }
      escaped = !escaped && char === '\\';
      continue;
    }

    if (inTemplate) {
      if (!escaped && char === '`') {
        inTemplate = false;
      }
      escaped = !escaped && char === '\\';
      continue;
    }

    escaped = false;

    if (char === '/' && next === '/') {
      inLineComment = true;
      index += 1;
      continue;
    }

    if (char === '/' && next === '*') {
      inBlockComment = true;
      index += 1;
      continue;
    }

    if (char === "'") {
      inSingle = true;
      continue;
    }

    if (char === '"') {
      inDouble = true;
      continue;
    }

    if (char === '`') {
      inTemplate = true;
      continue;
    }

    if (char === '{') {
      depth += 1;
    } else if (char === '}') {
      depth -= 1;
      if (depth === 0) {
        return index;
      }
    }
  }

  return content.length - 1;
}

function extractWorkflowJobs(content) {
  const lines = content.split(/\r?\n/);
  const jobs = [];
  let inJobsSection = false;

  for (const line of lines) {
    if (!inJobsSection) {
      if (line.trim() === 'jobs:') {
        inJobsSection = true;
      }
      continue;
    }

    if (/^\S/.test(line)) {
      break;
    }

    const match = line.match(/^\s{2}([A-Za-z0-9_-]+):\s*$/);
    if (match) {
      jobs.push(match[1]);
    }
  }

  return jobs;
}

function normalizeRouteTarget(value) {
  if (
    value.startsWith('(') ||
    value.startsWith('express.') ||
    value.startsWith('cookieParser') ||
    value.startsWith('session(') ||
    value.startsWith('this.')
  ) {
    return '<inline middleware>';
  }
  return value;
}

function normalizeSignature(value) {
  return String(value || '')
    .replace(/\s+/g, ' ')
    .trim();
}

function findMatches(regex, content, mapper) {
  const matches = [];
  let match;
  while ((match = regex.exec(content)) !== null) {
    const mapped = mapper(match);
    if (Array.isArray(mapped)) {
      matches.push(...mapped);
    } else if (mapped != null) {
      matches.push(mapped);
    }
  }
  return matches;
}

function findRegexWithLines(regex, content) {
  const lineIndex = createLineIndex(content);
  const results = [];
  let match;
  while ((match = regex.exec(content)) !== null) {
    results.push({
      match,
      line: lineNumberAt(lineIndex, match.index),
    });
  }
  return results;
}

function unique(items) {
  return [...new Set(items)];
}

function uniqueBy(items, keyFn) {
  const seen = new Set();
  const result = [];
  for (const item of items) {
    const key = keyFn(item);
    if (!seen.has(key)) {
      seen.add(key);
      result.push(item);
    }
  }
  return result;
}

